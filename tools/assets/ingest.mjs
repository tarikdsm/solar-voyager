import { copyFile, mkdir, rename, rm, stat, writeFile } from 'node:fs/promises';
import { basename, dirname, extname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { discoverAssets, validateAssetDirectory } from './assetIngest.mjs';
import {
  CATEGORY_CONFIG,
  SURFACE_DETAIL_CONFIG,
  assetByteBudget,
  guideReference,
} from './config.mjs';
import { encodeTexture as encodeKtxTexture } from './ktx.mjs';
import { compressGlb } from './processAsset.mjs';

const GENERATED_README = '# public/assets\n\nGenerated by `npm run assets:ingest`. Never edit these files by hand.\n';
const DEFAULT_THREE_ROOT = fileURLToPath(new URL('../../node_modules/three', import.meta.url));
const CODEC_FILES = Object.freeze([
  ['examples/jsm/libs/basis/basis_transcoder.js', 'codecs/basis/basis_transcoder.js'],
  ['examples/jsm/libs/basis/basis_transcoder.wasm', 'codecs/basis/basis_transcoder.wasm'],
  ['examples/jsm/libs/draco/gltf/draco_wasm_wrapper.js', 'codecs/draco/draco_wasm_wrapper.js'],
  ['examples/jsm/libs/draco/gltf/draco_decoder.wasm', 'codecs/draco/draco_decoder.wasm'],
  ['LICENSE', 'codecs/THREE-LICENSE.txt'],
]);

function sphereTierWidth(category) {
  if (category === 'planets') return 2048;
  if (['asteroids', 'comets', 'dwarfs', 'moons'].includes(category)) return 1024;
  return null;
}

async function copyRuntimeCodecs(threeRoot, stagingRoot) {
  for (const [sourceRelative, outputRelative] of CODEC_FILES) {
    const outputPath = join(stagingRoot, ...outputRelative.split('/'));
    await mkdir(dirname(outputPath), { recursive: true });
    await copyFile(join(threeRoot, ...sourceRelative.split('/')), outputPath);
  }
}

function textureRole(filename) {
  const stem = basename(filename, extname(filename));
  const suffix = stem.includes('_') ? stem.slice(stem.indexOf('_') + 1) : stem;
  if (suffix.startsWith('emissive')) return 'emissive';
  if (suffix.startsWith('detail_')) return suffix;
  return suffix.replace(/_night$/, '');
}

async function publishDirectory(stagingRoot, outputRoot) {
  const backupRoot = `${outputRoot}.ingest-backup`;
  await rm(backupRoot, { force: true, recursive: true });
  let hadPreviousOutput = true;
  try {
    await rename(outputRoot, backupRoot);
  } catch (error) {
    if (error instanceof Error && 'code' in error && error.code === 'ENOENT') hadPreviousOutput = false;
    else throw error;
  }
  try {
    await rename(stagingRoot, outputRoot);
    await rm(backupRoot, { force: true, recursive: true });
  } catch (error) {
    if (hadPreviousOutput) await rename(backupRoot, outputRoot);
    throw error;
  }
}

function canonicalManifest(assets) {
  return `${JSON.stringify({ schemaVersion: 2, assets }, null, 2)}\n`;
}

function surfaceDetailDescriptor(asset, files) {
  const albedo = `textures/${asset.id}_detail_albedo.ktx2`;
  const normal = `textures/${asset.id}_detail_normal.ktx2`;
  const hasAlbedo = files.includes(albedo);
  const hasNormal = files.includes(normal);
  const config = SURFACE_DETAIL_CONFIG[asset.id];
  if (config === undefined) {
    if (hasAlbedo || hasNormal) {
      throw new Error(`${asset.id}: surface-detail textures require explicit runtime scale metadata`);
    }
    return null;
  }
  if (!hasAlbedo || !hasNormal) {
    throw new Error(`${asset.id}: configured surface detail requires both runtime texture files`);
  }
  return { albedo, normal, tilesPerEquator: config.tilesPerEquator, seed: config.seed };
}

export async function ingestAssets(options) {
  const modelsRoot = options.modelsRoot;
  const outputRoot = options.outputRoot;
  const selectedIds = options.onlyIds === undefined ? null : new Set(options.onlyIds);
  const discovered = (await discoverAssets(modelsRoot))
    .filter((asset) => selectedIds === null || selectedIds.has(asset.id));
  if (discovered.length === 0) throw new Error(`No assets discovered under ${modelsRoot}`);

  const validated = [];
  const findings = [];
  for (const asset of discovered) {
    const validation = await validateAssetDirectory(asset.directory, asset);
    findings.push(...validation.findings);
    validated.push({ ...asset, ...validation });
  }
  if (findings.length > 0) {
    throw new Error(`Asset ingest validation failed:\n${findings.join('\n')}`);
  }

  const stagingRoot = `${outputRoot}.ingest-staging-${String(process.pid)}`;
  await rm(stagingRoot, { force: true, recursive: true });
  await mkdir(join(stagingRoot, 'models'), { recursive: true });
  await mkdir(join(stagingRoot, 'textures'), { recursive: true });

  const manifestAssets = [];
  try {
    await copyRuntimeCodecs(options.threeRoot ?? DEFAULT_THREE_ROOT, stagingRoot);
    for (const asset of validated) {
      const files = [];
      const textureBindings = [];
      for (const texture of asset.textures) {
        const textureOutputName = `${basename(texture, extname(texture))}.ktx2`;
        const textureRelative = `textures/${textureOutputName}`;
        await (options.encodeTexture ?? encodeKtxTexture)(
          join(asset.directory, texture),
          join(stagingRoot, 'textures', textureOutputName),
          { executable: options.ktxExecutable },
        );
        files.push(textureRelative);
        textureBindings.push({
          role: textureRole(texture),
          sourceName: texture,
          uri: `../${textureRelative}`,
        });
        const role = textureRole(texture);
        const tierWidth = role === 'albedo' ? sphereTierWidth(asset.category) : null;
        if (tierWidth !== null) {
          const sphereOutputName = `${asset.id}_albedo_tier2.ktx2`;
          const sphereRelative = `textures/${sphereOutputName}`;
          await (options.encodeTexture ?? encodeKtxTexture)(
            join(asset.directory, texture),
            join(stagingRoot, 'textures', sphereOutputName),
            {
              executable: options.ktxExecutable,
              width: tierWidth,
              height: tierWidth / 2,
            },
          );
          files.push(sphereRelative);
          for (const [suffix, width] of [
            ['2k', 2048],
            ['1k', 1024],
          ]) {
            const qualityOutputName = `${asset.id}_albedo_${suffix}.ktx2`;
            const qualityRelative = `textures/${qualityOutputName}`;
            await (options.encodeTexture ?? encodeKtxTexture)(
              join(asset.directory, texture),
              join(stagingRoot, 'textures', qualityOutputName),
              {
                executable: options.ktxExecutable,
                width,
                height: width / 2,
              },
            );
            files.push(qualityRelative);
          }
        }
      }
      const modelRelative = `models/${asset.id}.glb`;
      const modelOutput = join(stagingRoot, 'models', `${asset.id}.glb`);
      await (options.compressGlb ?? compressGlb)(
        join(asset.directory, `${asset.id}.glb`),
        modelOutput,
        { textures: textureBindings },
      );
      files.push(modelRelative);
      files.sort((left, right) => left.localeCompare(right, 'en'));

      let totalBytes = 0;
      for (const file of files) totalBytes += (await stat(join(stagingRoot, ...file.split('/')))).size;
      const limit = assetByteBudget(asset.category, asset.id);
      if (totalBytes > limit) {
        throw new Error(
          `${asset.id}: ${guideReference(9)} — ${totalBytes.toLocaleString('en-US')} bytes exceed ` +
          `${limit.toLocaleString('en-US')} byte budget`,
        );
      }

      const manifestAsset = {
        id: asset.id,
        category: CATEGORY_CONFIG[asset.category].manifestCategory,
        triangles: asset.triangles,
        files,
      };
      const surfaceDetail = surfaceDetailDescriptor(asset, files);
      manifestAssets.push(
        surfaceDetail === null ? manifestAsset : { ...manifestAsset, surfaceDetail },
      );
    }
    manifestAssets.sort((left, right) => left.id.localeCompare(right.id, 'en'));
    await writeFile(join(stagingRoot, 'manifest.json'), canonicalManifest(manifestAssets));
    await writeFile(join(stagingRoot, 'README.md'), GENERATED_README);
    await mkdir(dirname(outputRoot), { recursive: true });
    await publishDirectory(stagingRoot, outputRoot);
  } catch (error) {
    await rm(stagingRoot, { force: true, recursive: true });
    throw error;
  }

  return { assets: manifestAssets, outputRoot };
}
