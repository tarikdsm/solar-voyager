import { mkdir, rename, rm, stat, writeFile } from 'node:fs/promises';
import { basename, dirname, extname, join } from 'node:path';

import { discoverAssets, validateAssetDirectory } from './assetIngest.mjs';
import { CATEGORY_CONFIG, assetByteBudget, guideReference } from './config.mjs';
import { encodeTexture as encodeKtxTexture } from './ktx.mjs';
import { compressGlb } from './processAsset.mjs';

const GENERATED_README = '# public/assets\n\nGenerated by `npm run assets:ingest`. Never edit these files by hand.\n';

async function publishDirectory(stagingRoot, outputRoot) {
  const backupRoot = `${outputRoot}.ingest-backup`;
  await rm(backupRoot, { force: true, recursive: true });
  let hadPreviousOutput = true;
  try {
    await rename(outputRoot, backupRoot);
  } catch (error) {
    if (error instanceof Error && 'code' in error && error.code === 'ENOENT') hadPreviousOutput = false;
    else throw error;
  }
  try {
    await rename(stagingRoot, outputRoot);
    await rm(backupRoot, { force: true, recursive: true });
  } catch (error) {
    if (hadPreviousOutput) await rename(backupRoot, outputRoot);
    throw error;
  }
}

function canonicalManifest(assets) {
  return `${JSON.stringify({ schemaVersion: 1, assets }, null, 2)}\n`;
}

export async function ingestAssets(options) {
  const modelsRoot = options.modelsRoot;
  const outputRoot = options.outputRoot;
  const selectedIds = options.onlyIds === undefined ? null : new Set(options.onlyIds);
  const discovered = (await discoverAssets(modelsRoot))
    .filter((asset) => selectedIds === null || selectedIds.has(asset.id));
  if (discovered.length === 0) throw new Error(`No assets discovered under ${modelsRoot}`);

  const validated = [];
  const findings = [];
  for (const asset of discovered) {
    const validation = await validateAssetDirectory(asset.directory, asset);
    findings.push(...validation.findings);
    validated.push({ ...asset, ...validation });
  }
  if (findings.length > 0) {
    throw new Error(`Asset ingest validation failed:\n${findings.join('\n')}`);
  }

  const stagingRoot = `${outputRoot}.ingest-staging-${String(process.pid)}`;
  await rm(stagingRoot, { force: true, recursive: true });
  await mkdir(join(stagingRoot, 'models'), { recursive: true });
  await mkdir(join(stagingRoot, 'textures'), { recursive: true });

  const manifestAssets = [];
  try {
    for (const asset of validated) {
      const files = [];
      const modelRelative = `models/${asset.id}.glb`;
      const modelOutput = join(stagingRoot, 'models', `${asset.id}.glb`);
      await (options.compressGlb ?? compressGlb)(join(asset.directory, `${asset.id}.glb`), modelOutput);
      files.push(modelRelative);

      for (const texture of asset.textures) {
        const textureOutputName = `${basename(texture, extname(texture))}.ktx2`;
        const textureRelative = `textures/${textureOutputName}`;
        await (options.encodeTexture ?? encodeKtxTexture)(
          join(asset.directory, texture),
          join(stagingRoot, 'textures', textureOutputName),
          { executable: options.ktxExecutable },
        );
        files.push(textureRelative);
      }
      files.sort((left, right) => left.localeCompare(right, 'en'));

      let totalBytes = 0;
      for (const file of files) totalBytes += (await stat(join(stagingRoot, ...file.split('/')))).size;
      const limit = assetByteBudget(asset.category, asset.id);
      if (totalBytes > limit) {
        throw new Error(
          `${asset.id}: ${guideReference(9)} — ${totalBytes.toLocaleString('en-US')} bytes exceed ` +
          `${limit.toLocaleString('en-US')} byte budget`,
        );
      }

      manifestAssets.push({
        id: asset.id,
        category: CATEGORY_CONFIG[asset.category].manifestCategory,
        triangles: asset.triangles,
        files,
      });
    }
    manifestAssets.sort((left, right) => left.id.localeCompare(right.id, 'en'));
    await writeFile(join(stagingRoot, 'manifest.json'), canonicalManifest(manifestAssets));
    await writeFile(join(stagingRoot, 'README.md'), GENERATED_README);
    await mkdir(dirname(outputRoot), { recursive: true });
    await publishDirectory(stagingRoot, outputRoot);
  } catch (error) {
    await rm(stagingRoot, { force: true, recursive: true });
    throw error;
  }

  return { assets: manifestAssets, outputRoot };
}
